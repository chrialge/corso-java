
public class es8 {

    public static void main(String[] args) {

        // riporta il numero positivo
        System.out.println(Math.abs(-55));

        // riporta il numero piu piccolo
        System.out.println(Math.min(55, 32));

        // riporta il numero piu grande
        System.out.println(Math.max(55, 32));

        // riporta il numero elevato
        System.out.println(Math.pow(5, 2));

        // riporta la radice quadrata del numero
        System.out.println(Math.sqrt(25));

        // riporta il pi greco
        System.out.println(Math.PI);

    }
}
